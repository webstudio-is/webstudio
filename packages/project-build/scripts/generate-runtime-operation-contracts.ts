import { mkdir, writeFile } from "node:fs/promises";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import { builderRuntimeOperations } from "../src/runtime/registry";

const outputPath = join(
  dirname(fileURLToPath(import.meta.url)),
  "../src/contracts/__generated__/runtime-operation-contracts.ts"
);

const toSource = (value: unknown) => JSON.stringify(value, undefined, 2);

const contractData = builderRuntimeOperations.map(
  ({
    id,
    command,
    client,
    permit,
    kind,
    inputJsonSchema,
    outputJsonSchema,
    readNamespaces,
    writeNamespaces,
    invalidatesNamespaces,
    retryOnConflict,
    requiresAssets,
    requiresConfirm,
  }) => ({
    id,
    command,
    client,
    permit,
    kind,
    inputSchema: inputJsonSchema,
    outputSchema: outputJsonSchema,
    readNamespaces,
    writeNamespaces,
    invalidatesNamespaces,
    retryOnConflict,
    ...(requiresAssets ? { requiresAssets } : {}),
    ...(requiresConfirm ? { requiresConfirm } : {}),
  })
);

const source = `// This file is generated by scripts/generate-runtime-operation-contracts.ts.
// Do not edit manually.

export const runtimeOperationContractData = ${toSource(contractData)} as const;
`;

await mkdir(dirname(outputPath), { recursive: true });
await writeFile(outputPath, source);
